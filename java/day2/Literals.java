public class Literals {
    public static void main(String[] args) {
        // Integer literal
        int x = 10;
        System.out.println("Integer literal: " + x);

        // Floating-point literal
        double y = 10.5;
        System.out.println("Floating-point literal: " + y);

        // Character literal
        char z = 'A';
        System.out.println("Character literal: " + z);

        // String literal
        String s = "Hello, World!";
        System.out.println("String literal: " + s);

        // Boolean literal
        boolean b = true;
        System.out.println("Boolean literal: " + b);
    }
}






