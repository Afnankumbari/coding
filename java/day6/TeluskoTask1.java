package day6;

public class TeluskoTask1 {
    public static void main(String[] args) {

        //For Loop
        
        System.out.print("For Loop: ");


        for(int i=1; i<=5; i++)
            {
            System.out.print(i +" ");
        }
        //While Loop

        System.out.println();
        System.out.print("While Loop: ");

        int i=1;
        while(i<=5){
            System.out.print(i + " ");
            i++;
        }


        // Do - While loop
        System.out.println();
        System.out.print("Do-While Loop: ");

        
         int j = 1;  
        do{
            System.out.print(j +" ");
            j++;
        }
        while(j <= 5);
    }
    
}
